// Package set предоставляет реализацию типобезопасного множества (Set) на основе хэш-таблицы.
// Множество поддерживает операции объединения, пересечения, разности и симметрической разности.
package set

// Optimization определяет стратегию поведения при объединении множеств.
// Используется в функции UnionByStrategy.
const (
	// Optimization (значение по умолчанию) выбирает наиболее эффективную стратегию
	// в зависимости от размера множеств (клонируется меньшее множество).
	Optimization UnionStrategy = iota

	// CurrentSet при объединении клонирует исходное множество (CurrentSet)
	// и добавляет в него элементы входящего множества (IncomingSet).
	// Полезно, когда нужно сохранить приоритет элементов из текущего множества.
	CurrentSet

	// IncomingSet при объединении клонирует входящее множество (IncomingSet)
	// и добавляет в него элементы исходного множества (CurrentSet).
	// Полезно, когда входящее множество заведомо меньше или нужно сохранить его структуру.
	IncomingSet
)

// UnionStrategy определяет стратегию слияния двух множеств.
type UnionStrategy uint8
